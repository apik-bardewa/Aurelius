const User = require('../models/User');
const UserInterest = require('../models/UserInterest');
const InterestHistory = require('../models/InterestHistory');

// GET /api/users/profile
// Returns the logged-in user's profile and interests
const getProfile = async (req, res) => {
  try {
    const user = await User.findById(req.userId).select('-password');
    if (!user) return res.status(404).json({ message: 'User not found' });

    const interest = await UserInterest.findOne({ userId: req.userId });

    res.json({ user, interest });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// PUT /api/users/profile
// Body: { name }
// Update basic profile info
const updateProfile = async (req, res) => {
  try {
    const { name } = req.body;

    const user = await User.findByIdAndUpdate(
      req.userId,
      { name },
      { new: true }
    ).select('-password');

    res.json(user);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// POST /api/users/interests
// Body: { topics: ['AI', 'Finance', 'Sports'] }
// Called after signup to save selected interests
// Also calls Python embedding service to build profile vector
const setInterests = async (req, res) => {
  try {
    const { topics } = req.body;
    if (!topics || topics.length === 0) {
      return res.status(400).json({ message: 'Select at least one topic' });
    }

    // Save or update the UserInterest record
    let interest = await UserInterest.findOne({ userId: req.userId });

    const topicScoresBefore = interest ? interest.topicScores : {};
    const topicsBefore = interest ? interest.topics : [];

    if (interest) {
      interest.topics = topics;
      await interest.save();
    } else {
      interest = await UserInterest.create({
        userId: req.userId,
        topics,
        topicScores: {},
      });
    }

    // Save to User.interests as well
    await User.findByIdAndUpdate(req.userId, { interests: topics });

    // Call Python embedding service to generate profile vector
    // The embedding service encodes the topic labels and mean-pools them
    // into a single 384-dim vector stored in User.profileEmbedding
    try {
      const axios = require('axios');
      const response = await axios.post(
        `${process.env.PYTHON_SERVICE_URL}/embed/interests`,
        { topics }
      );
      const profileEmbedding = response.data.profile_vector;
      await User.findByIdAndUpdate(req.userId, { profileEmbedding });
    } catch (embedErr) {
      // Non-fatal — embedding will be generated by the background job
      console.warn('Embedding service unavailable:', embedErr.message);
    }

    // Log the change to InterestHistory
    await InterestHistory.create({
      userId: req.userId,
      trigger: interest ? 'manual_update' : 'signup',
      topicsBefore,
      topicsAfter: topics,
      topicScoresBefore,
      topicScoresAfter: interest.topicScores,
    });

    res.json({ message: 'Interests saved', interest });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// GET /api/users/interests
// Returns the user's current topic scores
const getInterests = async (req, res) => {
  try {
    const interest = await UserInterest.findOne({ userId: req.userId });
    if (!interest) return res.status(404).json({ message: 'No interests set yet' });
    res.json(interest);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

module.exports = { getProfile, updateProfile, setInterests, getInterests };
